  public static getPattern(...types: PatternType[]): GameInstance[] {
    if (types.length === 0) {
      types.push(
        PatternType.Glider,
      )
    }
    return types.map((type) => (
      new GameInstance(
        new Game(GameFactory.getGrid(type)),
        PatternType[type],
      )
    ))
  }
